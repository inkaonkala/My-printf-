#include "ft_printf.h"

int	printstr(char *s)
{
	int	i;

	i = 0;
	if (s)
	{
		while (*s != '\0')
		{
			printchar((int)*s);
			i++;
			s++;
		}
	}
	else
	{
		write (1, "(null)", 6);
		i = 6;
	}
	return (i);
}
